package com.yhb.annotation;

import java.lang.annotation.*;


/**
 * Copyright: Copyright (c) 2017 LanRu-Caifu
 * 
 * @ClassName: BaseService.java
 * @Description: 初始化继承BaseService的service @Documented用于描述其它类型的annotation应该被作为被标注的程序成员的公共API
 *               ，因此可以被例如javadoc此类的工具文档化。Documented是一个标记注解，没有成员
 * @Retention定义了该Annotation被保留的时间长短：某些Annotation仅出现在源代码中， 而被编译器丢弃; 而另一些却被编译在class文件中；
 *                                                        编译在class文件中的Annotation可能会被虚拟机忽略
 *                                                        ，而另一些在class被装载时将被读取（
 *                                                        请注意并不影响class的执行 ，
 *                                                        因为Annotation与class在使用上是被分离的
 *                                                        ）。使用这个meta-
 *                                                        Annotation可以对
 *                                                        Annotation的“ 生命周期”限制。
 * @author:Krx
 * @date: 2017年5月5日 下午2:08:18
 */
@Target({ ElementType.TYPE })
@Retention(RetentionPolicy.RUNTIME)
@Documented
public @interface BaseService {

}
